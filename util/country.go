package util

// Country is a coutnry structure.
type Country struct {
	// Cc is App Store country code
	Cc      string
	Emoji   string
	Title   string
	TitleRu string
	// Code is a ISO 3166-1 alpha-2 code
	Code string
}

// Translate translates country title
func (c Country) Translate(language string) string {
	switch language {
	case "en-us", "en-gb", "en-au":
		return c.Title
	case "ru-ru":
		return c.TitleRu
	}

	return c.Title
}

